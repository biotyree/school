#include <unistd.h>

void	ft_putnbr(int n)
{
	char	a;

	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else if (n < 0)
	{
		write(1, "-", 1);
		ft_putnbr(n * (-1));
	}
	else if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
	{
		a = '0' + n;
		write(1, &a, 1);
	}
}
