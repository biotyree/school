#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		q;

	q = 0;
	i = 0;
	if (n == 0)
		return (0);
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0' && i < n - 1)
		i++;
	if (s1[i] != s2[i])
		q = (unsigned char)s1[i] - (unsigned char)s2[i];
	return (q);
}
