#include <unistd.h>

void	ft_putendl(char const *s)
{
	unsigned int i;

	i = 0;
	if (s)
	{
		while (s[i])
		{
			write(1, &s[i], 1);
			i++;
		}
		write(1, "\n", 1);
	}
}
