int		ft_iterative_factorial(int nb)
{
	int a;
	int b;

	a = 1;
	b = 1;
	if (nb <= 12 && nb >= 0)
	{
		if (nb == 1 || nb == 0)
			return (1);
		else
		{
			while (a <= nb)
			{
				b = b * a;
				a++;
			}
			return (b);
		}
	}
	else
		return (0);
}
