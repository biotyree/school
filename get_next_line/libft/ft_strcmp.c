#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int i;
	int q;

	q = 0;
	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
		i++;
	if (s1[i] != s2[i])
		q = (unsigned char)s1[i] - (unsigned char)s2[i];
	return (q);
}
